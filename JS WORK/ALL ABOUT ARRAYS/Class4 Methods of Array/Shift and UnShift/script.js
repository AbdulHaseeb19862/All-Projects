// shift():
// The shift() method removes the first element from an array and returns it. The remaining elements are shifted to a lower index.

// unshift():
// The unshift() method adds one or more elements to the beginning of an array and returns the new length of the array.

let arr = ["Rahool", "Mohib", "Fayaz", "Haseeb Bangash"];
document.write(arr + "<br>");
arr.shift();
document.write(arr + "<br>");
arr.unshift("Abdul Haseeb Bangash");
document.write(arr + "<br>");
