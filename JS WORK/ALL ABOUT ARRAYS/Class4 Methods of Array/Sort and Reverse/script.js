// sort():
// The sort() method arranges the elements of an array in alphabetical or numerical order. By default, it sorts elements as strings.

const arr1 = [2, 1, 4, 3, 5];
document.write(arr1 + "<br><br>");
arr1.sort();
document.write(arr1 + "<br>");

// reverse():
// The reverse() method reverses the order of the elements in an array.
arr1.reverse();
document.write(arr1 + "<br>");
